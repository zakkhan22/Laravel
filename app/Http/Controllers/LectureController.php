    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //

// dd($request->all());
        

        $request->validate([
            'lec_name' => 'required|string',
            'c_code' => 'required|string|',
            'lec_desc' => 'required|string'
        ]);
        $lecture = new Lecture;
        $lecture->course_name = $request->lec_name;
        $lecture->course_code = $request->c_code;
        $lecture->course_description = $request->lec_desc;

        
        $lecture->save();
        

        session()->flash('success', 'Course registered successfully');

        return redirect('/admin');
    }

