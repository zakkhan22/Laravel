    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'teacher_name' => 'required|string',
            'teacher_reg_no' => 'required|string|unique:teachers',
            'teacher_password' => 'required|string'
        ]);
        $teacher = new Teacher;
        $teacher->teacher_name= $request->teacher_name;
        $teacher->teacher_reg_no = $request->teacher_reg_no;
        $teacher->teacher_password = $request->teacher_password;

        $teacher->save();

        session()->flash('success', 'teacher created successfully');

        return redirect('/admin');
    
    }

